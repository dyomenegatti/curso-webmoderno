const a = 7; //atribuindo o 7 a constante a
let b = 3;

b += a; //atribuição aditiva - acrescenta o valor de a em b
console.log(b);

b -= 4; //atribuição subtrativa - subtrai o valor 4 de b
console.log(b);

b *= 2; //atribuição multiplicativa - multiplica o valor 2 com o valor de b
console.log(b);

b /= 2; //atribuição divisiva - divide o valor 2 com o valor de b
console.log(b);

b %= 2; //atribuição modular 
console.log(b);