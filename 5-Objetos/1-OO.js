/* Principios importantes:
* 1) Abstração: Como trazer algo do mundo real para o código
* 2) Encapsulamento: Ter os detalhes de implementação escondidos e mostrar para quem precisa usar para que seja possível interagir com o objeto
* 3) Herança: Baseado na relação É-Um - Uma função (função filho) recebe caracteristicas, atributos e comportamentos de uma outra função (função pai)
* 4) Polimorfismo: Multiplas formas - A partir de um objeto genérico, é possível usar e criar objetos concretos
*/